                <h3 class="cont-tit">REGKIRONA (regdanvimab): Overview</h3>
                <p>Regdanvimab is a recombinant human IgG1 monoclonal antibody that binds to the receptor binding domain
                    (RBD) of the SARS-CoV-2 spike(s) protein. This interaction is thought to prevent SARS-CoV-2 from
                    binding to human epithelial and endothelial cells and causing COVID-19.<sup>1</sup></p>

                <p><br></p>

                <p>Clinical studies in patients with mild-to-moderate COVID-19 have demonstrated that regdanvimab 40
                    mg/kg shortens the time to clinical recovery<sup>&dagger;</sup> and reduces the risk of progression
                    to severe disease and/or hospitalisation<sup>&Dagger;</sup> compared to placebo. Regdanvimab was
                    generally well tolerated with no significant safety issues reported in the clinical development
                    programme.<sup>1-5</sup></p>

                <p><br></p>

                <p>In addition, <em>in vitro</em> and <em>in vivo</em> animal studies demonstrated that regdanvimab
                    retained activity against mutant viruses of concern, including Alpha (B.1.17), Zeta (P.2), Iota
                    (B.1.526) and Eta (B.1.525). There was reduced neutralising activity against Beta (B.1.351), Gamma
                    (P.1), Epsilon (B.1.427 and B.1.429), Kappa (B.1.617.1) and Delta (B.1.167.2).<sup>5-9</sup></p>

                <p><br></p>

