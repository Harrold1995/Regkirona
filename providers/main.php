            <h3 class="cont-tit">Warnings and precautions for use</h3>
            <div class="cont-indt">
                <b class="p-bk mg10">Contraindications</b>
                <ul class="cont-list">
                    <li>Regdanvimab is contraindicated in patients with a history of hypersensitivity to the active
                        substance or any of the excipients (e.g., histidine, histidine hydrochloride monohydrate,
                        polysorbate 80, aginine hydrochloride)</li>
                </ul>
                <br>
                <b class="p-bk mg10">Hypersensitivity including infusion-related reactions</b>
                <ul class="cont-list">
                    <li>Serious hypersensitivity reactions, including anaphylaxis, have been reported with
                        administration of regdanvimab. If signs and symptoms of a clinically significant
                        hypersensitivity reaction or anaphylaxis occur, administration of regdanvimab should be
                        discontinued and appropriate supportive treatment should be administered</li>
                </ul> <br>
                <b class="p-bk mg10">Antiviral resistance</b>
                <ul class="cont-list">
                    <li>The clinical trials with regdanvimab were conducted in subjects who were predominantly infected
                        with the wild-type virus and the Alpha (UK origin/B.1.1.7 lineage) variant. The clinical
                        efficacy data for regdanvimab against some circulating SARS-CoV-2 variants with decreased
                        susceptibility is currently limited. Healthcare professionals should consider the prevalence of
                        SARS-CoV-2 variants in their area, where data are available, when considering treatment options
                    </li>
                </ul> <br>
                <b class="p-bk mg10">Interactions with other medicines</b>
                <ul class="cont-list">
                    <li>The efficacy and safety of regdanvimab in subjects who have received a COVID-19 vaccine has not
                        been established</li>
                </ul> <br> <br>
            </div>

